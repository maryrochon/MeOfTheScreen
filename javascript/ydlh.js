$("#ydlh1").hover(function(){
    $(".graph").toggleClass("ydlh1")
    $(".flash").toggleClass("move1")
    if($("#ydlh1:hover").length == 1) {
    $("#pink1").css("opacity", "1")
    }
 else{
    $("#pink1").css("opacity", "0")
 }
})
